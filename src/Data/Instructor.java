package Data;

public class Instructor {
        private String instructor_uname;
        private String instructor_pw;
        private String instructor_email;

        public String getInstructor_uname() {
            return instructor_uname;
        }

        public void setInstructor_uname(String instructor_uname) {
            this.instructor_uname = instructor_uname;
        }

        public String getInstructor_pw() {
            return instructor_pw;
        }

        public void setInstructor_pw(String instructor_pw) {
            this.instructor_pw = instructor_pw;
        }

        public String getInstructor_email() {
            return instructor_email;
        }

        public void setInstructor_email(String instructor_email) {
            this.instructor_email = instructor_email;
        }

        public String getInstructor_fname() {
            return instructor_fname;
        }

        public void setInstructor_fname(String instructor_fname) {
            this.instructor_fname = instructor_fname;
        }

        public String getInstructor_lname() {
            return instructor_lname;
        }

        public void setInstructor_lname(String instructor_lname) {
            this.instructor_lname = instructor_lname;
        }

        private String instructor_fname;
        private String instructor_lname;
    }

