package Data;

public class student_work_product {
    private String title;
    private int student_work_product_id;
    private int grade;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getStudent_work_product_id() {
        return student_work_product_id;
    }

    public void setStudent_work_product_id(int student_work_product_id) {
        this.student_work_product_id = student_work_product_id;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }
}
